#include "libft.h"

void	*ft_memset(void *ptr, int n, size_t num)
{
	size_t	i;

	i = 0;
	while (i < num)
	{
		*(unsigned char *)(ptr + i) = (unsigned char)n;
		i++;
	}
	return (ptr);
}
